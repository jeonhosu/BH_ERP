CREATE OR REPLACE VIEW HRM_PERSON_ALL_V
AS
SELECT PM.PERSON_ID
     , PM.PERSON_NUM
     , PM.NAME
     , PM.CORP_ID
     , 0 AS DISPATCH_CORP_ID
     , PM.DEPT_ID
     , PM.NATION_ID
     , PM.WORK_TYPE_ID
     , PM.JOB_CLASS_ID
     , PM.JOB_ID
     , PM.POST_ID
     , PM.OCPT_ID
     , PM.ABIL_ID
     , PM.PAY_GRADE_ID
     , PM.REPRE_NUM
     , PM.SEX_TYPE
     , PM.JOIN_ID
     , PM.JOIN_ROUTE_ID
     , PM.ORI_JOIN_DATE
     , PM.JOIN_DATE
     , PM.RETIRE_DATE
     , PM.RETIRE_ID
     , PM.DIR_INDIR_TYPE
     , PM.EMPLOYE_TYPE
     , PM.TELEPHON_NO
     , PM.HP_PHONE_NO
     , PM.JOB_CATEGORY_ID
     , PM.FLOOR_ID
     , PM.COST_CENTER_ID
     , PM.CORP_TYPE
     , PM.DISPLAY_NAME
     , PM.SOB_ID     
     , PM.ORG_ID
  FROM HRM_PERSON_MASTER PM
UNION ALL
SELECT PD.PERSON_ID
     , PD.PERSON_NUM
     , PD.NAME
     , PD.CORP_ID
     , PD.DISPATCH_CORP_ID
     , PD.DEPT_ID
     , PD.NATION_ID
     , PD.WORK_TYPE_ID
     , PD.JOB_CLASS_ID
     , PD.JOB_ID
     , PD.POST_ID
     , PD.OCPT_ID
     , PD.ABIL_ID
     , PD.PAY_GRADE_ID
     , PD.REPRE_NUM
     , PD.SEX_TYPE
     , PD.JOIN_ID
     , PD.JOIN_ROUTE_ID
     , PD.ORI_JOIN_DATE
     , PD.JOIN_DATE
     , PD.RETIRE_DATE
     , PD.RETIRE_ID
     , PD.DIR_INDIR_TYPE
     , PD.EMPLOYE_TYPE
     , PD.TELEPHON_NO
     , PD.HP_PHONE_NO
     , PD.JOB_CATEGORY_ID
     , PD.FLOOR_ID
     , PD.COST_CENTER_ID
     , PD.CORP_TYPE
     , PD.DISPLAY_NAME
     , PD.SOB_ID     
     , PD.ORG_ID
  FROM HRM_PERSON_DISPATCH PD
;
