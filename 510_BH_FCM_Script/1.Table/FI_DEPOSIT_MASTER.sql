  ISSUE_DATE                    DATE            NOT NULL, -- 개설일자.
  DUE_DATE                      DATE            , -- 만기일자.
  CURRENCY_CODE                 VARCHAR2(10)    , -- 통화.
  EXCHANGE_RATE                 NUMBER          , -- 환율.
  DEPOSIT_CURR_AMOUNT           NUMBER          DEFAULT 0, -- 금액_외화.
  DEPOSIT_AMOUNT                NUMBER          DEFAULT 0, -- 금액.  
  INTER_RATE                    NUMBER          , -- 이자율.
  SPREAD_RATE                   NUMBER          , -- SPREAD_RATE.
  INTER_KIND                    VARCHAR2(10)    , -- 이자종류.
  INTER_TYPE                    VARCHAR2(10)    , -- 이자유형.
  PAYMENT_PERIOD                VARCHAR2(10)    , -- 납입주기.
  PAYMENT_COUNT                 NUMBER          , -- 납입횟수.
  MONTH_CURR_AMOUNT             NUMBER          DEFAULT 0, -- 월납입금액_외화.
  MONTH_AMOUNT                  NUMBER          DEFAULT 0, -- 월납입금액. 
  TOTAL_PAYMENT_COUNT           NUMBER          , -- 총납입횟수. 
  TOTAL_CURR_AMOUNT             NUMBER          DEFAULT 0, -- 총납입금액_외화.
  TOTAL_AMOUNT                  NUMBER          DEFAULT 0, -- 총납입금액.  
  CANCEL_DATE                   DATE            , -- 해약일자.
  CANCEL_EXCHANGE_RATE          NUMBER          , -- 해약시환율. 
  CANCEL_PRIN_AMOUNT            NUMBER          , -- 해약시원금.
  CANCEL_PRIN_CURR_AMOUNT       NUMBER          , -- 해약시원금_외화.
  CANCEL_INTER_RATE             NUMBER          , -- 해약시이자율.
  CANCEL_INTER_AMOUNT           NUMBER          , -- 해약시이금액.
  CANCEL_INTER_CURR_AMOUNT      NUMBER          , -- 해약시이금액_외화.
  CANCEL_AMOUNT                 NUMBER          , -- 해약금액.
  CANCEL_CURR_AMOUNT            NUMBER          , -- 해약금액_외화.
  MORTGAGE_YN                   VARCHAR2(1)     DEFAULT 'N', -- 담보설정(Y/N).
  MORTGAGE_DATE                 DATE            , -- 담보제공일자.
  FINAL_CURR_AMOUNT             NUMBER          DEFAULT 0, -- 최종금액_외화.
  FINAL_AMOUNT                  NUMBER          DEFAULT 0, -- 최종금액.  
  TRANS_STATUS                  VARCHAR2(10)    NOT NULL, -- 거래_상태.
  COST_CENTER_ID                NUMBER          , -- 코스트(COST)센타코드.
  REMARK                        VARCHAR2(100)   , -- 적요.
  ENABLED_FLAG                  VARCHAR2(1)     DEFAULT 'Y', -- 사용(Y/N).
  CREATION_DATE                 DATE            NOT NULL, -- 생성자.
  CREATED_BY                    NUMBER          NOT NULL, -- 생성일.
  LAST_UPDATE_DATE              DATE            NOT NULL, -- 수정자.
  LAST_UPDATED_BY               NUMBER          NOT NULL  -- 수정일.
) TABLESPACE FCM_TS_DATA;

