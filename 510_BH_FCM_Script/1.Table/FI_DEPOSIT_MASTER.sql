CREATE TABLE FI_DEPOSIT_MASTER 
( BANK_ID                       NUMBER          NOT NULL, -- 금융기관ID.
  BANK_ACCOUNT_ID               NUMBER          NOT NULL, -- 은행계좌ID.
  SOB_ID                        NUMBER          NOT NULL, -- 회계조직.
  ORG_ID                        NUMBER          NOT NULL, -- 사업부ID.
  DEPOSIT_GB                    VARCHAR2(10)    NOT NULL, -- 예적금구분(DEPOSIT_GB).
