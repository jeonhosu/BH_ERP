-- 테이블명: APPS.GL_BANK_MST
-- 테이블설명: 금융기관MASTER

DROP TABLE GL_BANK_MST CASCADE CONSTRAINTS ;

CREATE TABLE GL_BANK_MST (
  BANK_ID	        NUMBER
  BANK_CD           	VARCHAR2(10)    NOT NULL,	/* 금융기관코드 */
  KOR_NAME           	VARCHAR2(40)    ,	/* 금융기관명(한글) */
  ENG_NAME        	  VARCHAR2(40)    ,	/* 금융기관명(영문) */
  TYPE_CD           	CHAR(1)         ,	/* 금융기관유형 */
  CUST_CD           	VARCHAR2(10)    ,	/* 사업자 등록번호 */
  DC_LIMIT_AMT        NUMBER(11)      ,	/* 할인한도금액 */
  DC_RATE1          	NUMBER(7,4)     ,	/* 할인율(90일 이전) */
  LOAN_LIMIT_AMT     	NUMBER(11)      ,	/* 차월한도금액 */
  START_DATE        	DATE            ,	/* 거래시작일 */
  DC_RATE2          	NUMBER(7,4)     ,	/* 할인율(90일 이후) */
  DC_METH           	CHAR(1)         ,	/* 할인료계산방법 */
  
  CREATION_DATE        DATE             NOT NULL,/* 생성자 */
  CREATED_BY           NUMBER           NOT NULL,/* 생성일 */
  LAST_UPDATE_DATE     DATE             NOT NULL,/* 수정자 */
  LAST_UPDATED_BY      NUMBER           NOT NULL /* 수정일 */
);

COMMENT ON TABLE GL_BANK_MST IS '금융기관MASTER';
COMMENT ON COLUMN APPS.GL_BANK_MST.BANK_CODE IS '금융기관코드';
COMMENT ON COLUMN APPS.GL_BANK_MST.BANK_NAME IS '금융기관명(한글)';
COMMENT ON COLUMN APPS.GL_BANK_MST.BANK_ENAME IS '금융기관명(영문)';
COMMENT ON COLUMN APPS.GL_BANK_MST.BANK_TYPE IS '금융기관유형';
COMMENT ON COLUMN APPS.GL_BANK_MST.CUST_CD IS '사업자 등록번호';
COMMENT ON COLUMN APPS.GL_BANK_MST.DC_LIMIT_AMT IS '할인한도금액';
COMMENT ON COLUMN APPS.GL_BANK_MST.DC_RATE1 IS '할인율(90일 이전)';
COMMENT ON COLUMN APPS.GL_BANK_MST.LOAN_LIMIT_AMT IS '차월한도금액';
COMMENT ON COLUMN APPS.GL_BANK_MST.START_DATE IS '거래시작일';
COMMENT ON COLUMN APPS.GL_BANK_MST.DC_RATE2 IS '할인율(90일 이후)';
COMMENT ON COLUMN APPS.GL_BANK_MST.DC_METH  IS '할인료계산방법';

COMMENT ON COLUMN APPS.GL_BANK_MST.CREATION_DATE IS '생성자';
COMMENT ON COLUMN APPS.GL_BANK_MST.CREATED_BY IS '생성일';
COMMENT ON COLUMN APPS.GL_BANK_MST.LAST_UPDATE_DATE IS '수정자';
COMMENT ON COLUMN APPS.GL_BANK_MST.LAST_UPDATED_BY IS '수정일';


